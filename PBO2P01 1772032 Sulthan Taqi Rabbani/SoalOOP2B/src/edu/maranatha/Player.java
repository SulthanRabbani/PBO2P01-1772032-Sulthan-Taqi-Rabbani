package edu.maranatha;

/**
 *
 * @author master
 */
public class Player {

    private int win;
    private int draw;
    private int lose;

    public Player() {
    }

    public int getWin() {
        return win;
    }

    public void setWin(int win) {
        this.win = win;
    }

    public int getDraw() {
        return draw;
    }

    public void setDraw(int draw) {
        this.draw = draw;
    }

    public int getLose() {
        return lose;
    }

    public void setLose(int lose) {
        this.lose = lose;
    }

}
